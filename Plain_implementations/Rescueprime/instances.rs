fn f_from_hex<F: PrimeField>(hex: &str) -> Option<F> {
    let s = hex.strip_prefix("0x").unwrap_or(hex);
    let value = BigUint::parse_bytes(s.as_bytes(), 16).expect("invalid hex field element");
    Some(F::from_biguint(&value))
}

